# This module is responsible for performing analysis on the musical score, extracting useful information
# There will be tentatively 7 types of analysis
# Melody - Extract the melody from the score
# Harmony - Extract the harmonic progression from the score
# Rhythm - Extract the rhythm from the score
# Structure - Extract and analyse big similar sections of the score to find the structure
# Texture - Extract the texture of the score
# Dynamics - Extract the dynamics of the score
# Tempo - Extract the tempo of the score
